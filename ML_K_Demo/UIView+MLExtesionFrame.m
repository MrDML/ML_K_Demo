#import "UIView+MLExtesionFrame.h"

@implementation UIView (MLExtesionFrame)

- (void)setX:(CGFloat)x
{
    CGRect rect = self.frame;
    rect.origin.x = x;
    self.frame = rect;
}

- (CGFloat)x
{
    return self.frame.origin.x;
}



@end
